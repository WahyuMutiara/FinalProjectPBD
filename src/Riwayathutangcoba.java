public class Riwayathutangcoba {
    private int idRiwayat_hutang, id_barang, id_pembeli, jumlah_hutang, jumlahSisa_hutang;
    private String status_hutang, tgl_pembayaran;

    public int getIdRiwayat_hutang() {
        return idRiwayat_hutang;
    }

    public void setIdRiwayat_hutang(int idRiwayat_hutang) {
        this.idRiwayat_hutang = idRiwayat_hutang;
    }

    public int getId_barang() {
        return id_barang;
    }

    public void setId_barang(int id_barang) {
        this.id_barang = id_barang;
    }

    public int getId_pembeli() {
        return id_pembeli;
    }

    public void setId_pembeli(int id_pembeli) {
        this.id_pembeli = id_pembeli;
    }

    public int getJumlah_hutang() {
        return jumlah_hutang;
    }

    public void setJumlah_hutang(int jumlah_hutang) {
        this.jumlah_hutang = jumlah_hutang;
    }

    public int getJumlahSisa_hutang() {
        return jumlahSisa_hutang;
    }

    public void setJumlahSisa_hutang(int jumlahSisa_hutang) {
        this.jumlahSisa_hutang = jumlahSisa_hutang;
    }

    public String getStatus_hutang() {
        return status_hutang;
    }

    public void setStatus_hutang(String status_hutang) {
        this.status_hutang = status_hutang;
    }

    public String getTgl_pembayaran() {
        return tgl_pembayaran;
    }

    public void setTgl_pembayaran(String tgl_pembayaran) {
        this.tgl_pembayaran = tgl_pembayaran;
    }
    
    
}
